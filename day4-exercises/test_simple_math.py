import simple_math

def test_simple_math():
 assert simple_math.simple_add(1, 2) == 3

 assert simple_math.simple_sub(10, 5) == 5

 assert simple_math.simple_mult(3, 12) == 36

 assert simple_math.simple_div(100, 4) == 25

 assert simple_math.poly_first(2, 5, 3) == 11

 assert simple_math.poly_second(3, 4, 5, 10) == 109
